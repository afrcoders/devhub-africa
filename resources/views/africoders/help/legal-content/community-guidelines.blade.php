    <h3>Our Community Vision</h3>
    <p>Africoders is built to foster a vibrant, inclusive, and supportive community of professionals across Africa and beyond. These guidelines help ensure everyone can participate safely and productively.</p>

    <h3>Core Principles</h3>

    <h4>Respect and Inclusivity</h4>
    <ul>
        <li>Treat all community members with dignity and respect</li>
        <li>Welcome people from all backgrounds, experiences, and skill levels</li>
        <li>Embrace diverse perspectives and constructive debate</li>
        <li>Avoid discrimination based on race, gender, religion, nationality, or other characteristics</li>
        <li>Use inclusive language that makes everyone feel welcome</li>
    </ul>

    <h4>Professional Conduct</h4>
    <ul>
        <li>Maintain a professional tone in all interactions</li>
        <li>Provide constructive feedback and criticism</li>
        <li>Share knowledge generously and help others learn</li>
        <li>Be honest about your skills, experience, and qualifications</li>
        <li>Respect confidentiality when appropriate</li>
    </ul>

    <h4>Quality Content</h4>
    <ul>
        <li>Share relevant, valuable, and original content</li>
        <li>Fact-check information before sharing</li>
        <li>Give proper credit and attribution</li>
        <li>Avoid spam, self-promotion, and low-quality posts</li>
        <li>Use clear, professional language</li>
    </ul>

    <h3>Prohibited Behavior</h3>

    <h4>Harassment and Abuse</h4>
    <ul>
        <li>Personal attacks, insults, or threatening language</li>
        <li>Doxxing or sharing private information without consent</li>
        <li>Stalking or unwanted contact</li>
        <li>Sexual harassment or inappropriate advances</li>
        <li>Bullying or intimidation tactics</li>
    </ul>

    <h4>Discrimination and Hate</h4>
    <ul>
        <li>Hate speech based on protected characteristics</li>
        <li>Discriminatory language or behavior</li>
        <li>Content that promotes intolerance or extremism</li>
        <li>Stereotyping or prejudicial comments</li>
    </ul>

    <h4>Misinformation and Fraud</h4>
    <ul>
        <li>Deliberately spreading false information</li>
        <li>Impersonating others or creating fake profiles</li>
        <li>Fraudulent claims about credentials or experience</li>
        <li>Misleading business practices or scams</li>
    </ul>

    <h3>Content Guidelines</h3>

    <h4>Professional Posts</h4>
    <ul>
        <li>Share industry insights, career tips, and professional experiences</li>
        <li>Ask thoughtful questions and provide helpful answers</li>
        <li>Celebrate achievements and milestones appropriately</li>
        <li>Contribute to meaningful discussions</li>
    </ul>

    <h4>Educational Content</h4>
    <ul>
        <li>Share learning resources and educational opportunities</li>
        <li>Provide tutorials, guides, and how-to content</li>
        <li>Discuss new technologies and industry trends</li>
        <li>Mentor others and share knowledge freely</li>
    </ul>

    <h4>Networking and Collaboration</h4>
    <ul>
        <li>Make genuine connections and build relationships</li>
        <li>Share relevant job opportunities and projects</li>
        <li>Seek and offer collaboration opportunities</li>
        <li>Support community members' professional growth</li>
    </ul>

    <h3>Moderation and Enforcement</h3>

    <h4>Community Moderation</h4>
    <ul>
        <li>Our moderation team reviews reported content and behavior</li>
        <li>Community members can report violations using our reporting tools</li>
        <li>We investigate all reports fairly and thoroughly</li>
        <li>Actions are taken based on the severity and frequency of violations</li>
    </ul>

    <h4>Consequences for Violations</h4>
    <ul>
        <li><strong>Warning:</strong> First-time or minor violations</li>
        <li><strong>Content removal:</strong> Posts that violate guidelines</li>
        <li><strong>Temporary restrictions:</strong> Limited platform access</li>
        <li><strong>Account suspension:</strong> Serious or repeated violations</li>
        <li><strong>Permanent ban:</strong> Severe violations or repeated offenses</li>
    </ul>

    <h3>Reporting Issues</h3>
    <p>Help us maintain a positive community by reporting:</p>
    <ul>
        <li>Content that violates these guidelines</li>
        <li>Suspicious or fraudulent behavior</li>
        <li>Technical issues or bugs</li>
        <li>Suggestions for community improvement</li>
    </ul>

    <h4>How to Report</h4>
    <ul>
        <li>Use the report button on posts and profiles</li>
        <li>Contact our <a href="{{ route('help.contact') }}">support team</a></li>
        <li>Send details about the issue and relevant evidence</li>
    </ul>

    <h3>Appeals Process</h3>
    <p>If you believe a moderation action was taken in error:</p>
    <ul>
        <li>Review the specific guideline you're accused of violating</li>
        <li>Gather any relevant evidence or context</li>
        <li>Submit an appeal through our <a href="{{ route('help.contact') }}">contact form</a></li>
        <li>Wait for our team to review and respond</li>
    </ul>

    <h3>Community Leadership</h3>
    <p>We encourage active community members to:</p>
    <ul>
        <li>Model positive behavior for others</li>
        <li>Help newcomers understand our community norms</li>
        <li>Contribute to constructive discussions</li>
        <li>Report violations when you see them</li>
        <li>Provide feedback on community improvements</li>
    </ul>

    <h3>Updates to Guidelines</h3>
    <p>These guidelines may be updated to reflect:</p>
    <ul>
        <li>Community feedback and suggestions</li>
        <li>Changes in platform features or policies</li>
        <li>Legal or regulatory requirements</li>
        <li>Emerging community needs or challenges</li>
    </ul>

    <h3>Questions and Feedback</h3>
    <p>We welcome your input on these community guidelines. Please <a href="{{ route('help.contact') }}">contact us</a> with questions, suggestions, or feedback.</p>
