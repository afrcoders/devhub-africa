    <h3>Our Commitment</h3>
    <p>Africoders is committed to providing a harassment-free experience for everyone, regardless of age, body size, visible or invisible disability, ethnicity, sex characteristics, gender identity and expression, level of experience, education, socio-economic status, nationality, personal appearance, race, religion, or sexual identity and orientation.</p>

    <h3>Our Standards</h3>
    <p>Examples of behavior that contributes to a positive environment include:</p>

    <h4>Positive Behaviors</h4>
    <ul>
        <li>Demonstrating empathy and kindness toward other people</li>
        <li>Being respectful of differing opinions, viewpoints, and experiences</li>
        <li>Giving and gracefully accepting constructive feedback</li>
        <li>Accepting responsibility and apologizing to those affected by our mistakes</li>
        <li>Focusing on what is best not just for us as individuals, but for the overall community</li>
        <li>Using welcoming and inclusive language</li>
        <li>Respecting cultural differences and diverse perspectives</li>
    </ul>

    <h4>Professional Excellence</h4>
    <ul>
        <li>Sharing knowledge and expertise generously</li>
        <li>Mentoring newcomers and less experienced professionals</li>
        <li>Providing accurate information and citing sources</li>
        <li>Admitting when you don't know something</li>
        <li>Continuous learning and professional development</li>
        <li>Ethical business practices and fair dealing</li>
    </ul>

    <h3>Unacceptable Behavior</h3>
    <p>Examples of unacceptable behavior include:</p>

    <h4>Harassment and Discrimination</h4>
    <ul>
        <li>The use of sexualized language or imagery, and sexual attention or advances</li>
        <li>Trolling, insulting or derogatory comments, and personal or political attacks</li>
        <li>Public or private harassment of any kind</li>
        <li>Publishing others' private information without explicit permission</li>
        <li>Discriminatory jokes, language, or behavior</li>
        <li>Unwelcome comments regarding a person's lifestyle choices and practices</li>
    </ul>

    <h4>Professional Misconduct</h4>
    <ul>
        <li>Deliberate misinformation or false claims about credentials</li>
        <li>Plagiarism or intellectual property theft</li>
        <li>Breach of confidentiality or non-disclosure agreements</li>
        <li>Unethical business practices or fraud</li>
        <li>Spam, excessive self-promotion, or commercial exploitation</li>
        <li>Other conduct which could reasonably be considered inappropriate in a professional setting</li>
    </ul>

    <h3>Enforcement Responsibilities</h3>
    <p>Community leaders and moderators are responsible for clarifying and enforcing our standards of acceptable behavior and will take appropriate and fair corrective action in response to any behavior that they deem inappropriate, threatening, offensive, or harmful.</p>

    <h4>Moderator Authority</h4>
    <ul>
        <li>Remove, edit, or reject comments, posts, and other contributions</li>
        <li>Temporarily or permanently restrict accounts for policy violations</li>
        <li>Investigate reports of misconduct thoroughly and fairly</li>
        <li>Provide guidance and education about community standards</li>
        <li>Escalate serious violations to appropriate authorities when necessary</li>
    </ul>

    <h3>Scope</h3>
    <p>This Code of Conduct applies within all community spaces, including:</p>
    <ul>
        <li>All Africoders platform features and services</li>
        <li>Official social media accounts and communications</li>
        <li>Community events, meetups, and conferences</li>
        <li>Private communications between community members when they relate to community interactions</li>
        <li>Public spaces when an individual is officially representing the community</li>
    </ul>

    <h3>Enforcement Process</h3>

    <h4>Reporting</h4>
    <p>Instances of abusive, harassing, or otherwise unacceptable behavior may be reported to:</p>
    <ul>
        <li>The report function on posts and profiles</li>
        <li>Our <a href="{{ route('help.contact') }}">community support team</a></li>
        <li>Direct message to community moderators</li>
        <li>Email to our conduct enforcement team</li>
    </ul>

    <h4>Investigation</h4>
    <p>All complaints will be:</p>
    <ul>
        <li>Reviewed and investigated promptly and fairly</li>
        <li>Handled with confidentiality and discretion</li>
        <li>Addressed without retaliation against the reporter</li>
        <li>Documented for pattern recognition and community safety</li>
    </ul>

    <h4>Enforcement Actions</h4>
    <p>Depending on the severity and frequency of violations, actions may include:</p>
    <ul>
        <li><strong>Correction:</strong> A private or public clarification about the nature of the violation</li>
        <li><strong>Warning:</strong> A formal warning with consequences for continued behavior</li>
        <li><strong>Temporary Ban:</strong> A temporary ban from community interactions</li>
        <li><strong>Permanent Ban:</strong> A permanent ban from all community interactions</li>
    </ul>

    <h3>Community Leadership</h3>
    <p>We encourage all community members to:</p>
    <ul>
        <li>Model positive behavior and professional conduct</li>
        <li>Speak up when they witness violations of this code</li>
        <li>Support fellow community members who experience harassment</li>
        <li>Contribute to creating an inclusive and welcoming environment</li>
        <li>Provide constructive feedback to help improve our community</li>
    </ul>

    <h3>Acknowledgment</h3>
    <p>This Code of Conduct is adapted from the Contributor Covenant, version 2.0, and incorporates best practices from various professional communities and organizations committed to fostering inclusive environments.</p>

    <h3>Updates and Amendments</h3>
    <p>This Code of Conduct may be updated periodically to:</p>
    <ul>
        <li>Reflect community feedback and evolving needs</li>
        <li>Address new forms of misconduct or harassment</li>
        <li>Align with legal and regulatory requirements</li>
        <li>Incorporate lessons learned from enforcement experiences</li>
    </ul>

    <h3>Contact Information</h3>
    <p>For questions about this Code of Conduct, reporting violations, or providing feedback, please <a href="{{ route('help.contact') }}">contact our community team</a>.</p>
